package Gun15;

public class _02_JavaNestedLoop {
    public static void main(String[] args) {
        // 2 lerin carpim tablosunu sormustuk
        // 2 x 1 = 2
        // 2 x 2 = 4
        // ...
        // 5 lere kadar olan halini  yapiniz

        ;

        for (int j = 1; j <= 5; j++) {

            for (int i = 1; i <= 10; i++) {
                System.out.println(j + " x " + i + " = " + (j * i));
            }
            System.out.println();



        }



    }
}
