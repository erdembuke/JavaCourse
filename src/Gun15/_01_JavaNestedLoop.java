package Gun15;

public class _01_JavaNestedLoop {
    public static void main(String[] args) {
        /*  Asagidaki gorunumu tek yildiz kullanarak yazdirin.

        *****
        *****
        *****
        *****
        *****

         */

        for (int j = 0; j < 5; j++) {

            for (int i = 0; i < 5; i++)
                System.out.print("*");
            System.out.println();


        }

    }
}
