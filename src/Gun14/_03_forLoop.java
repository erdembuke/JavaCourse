package Gun14;

public class _03_forLoop {
    public static void main(String[] args) {
        // 0 dan 10(dahil) kadar sayilari ekrana yazdirin
        // 10 dan 0(dahil) kadar sayilari ekrana yazdirin
        int i = 0;
        for (i = 0; i <= 10; i++)
            System.out.println("sayi = " + i);

        System.out.println();

        for (i = 10; i >=0; i--)
            System.out.println("sayi = " + i);

    }
}
